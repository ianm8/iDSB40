  static const int16_t agc_dc(const int16_t in)
  {
    static int32_t s = 0;
    static int32_t x1 = 0;
    static int16_t y1 = 0;
    s -= x1;
    x1 = (int32_t)in << 16;
    s += x1 - ((int32_t)y1 << 11);
    return (y1 = s >> 16);
  }

  volatile static uint32_t peak = 0;

  const uint8_t agc_pwm(const int16_t sig)
  {
    static const uint32_t AGC_DECAY_RATE = 40ul;
    static const uint32_t AGC_HANG_TIME = 250ul;
    static uint32_t agc_next_update = 0;
    static uint32_t agc_hangtime_update = 0;
    const int16_t dc_sig = agc_dc(sig);
    const uint32_t level = ((uint32_t)abs(dc_sig));
    const uint32_t now = millis();
    if (level>peak)
    {
      peak = level;
      agc_next_update = now+AGC_DECAY_RATE;
      agc_hangtime_update = now+AGC_HANG_TIME;
    }
    else
    {
      if (now>agc_hangtime_update)
      {
        if (now>agc_next_update)
        {
          //const uint32_t decay_init = peak>>7;
          const uint32_t decay_init = peak>>6;
          const uint32_t decay_rate = decay_init==0?1:decay_init;
          if (peak>=decay_rate) peak -= decay_rate;
          agc_next_update = now+AGC_DECAY_RATE;
        }
      }
    }
    return agc_tab[min(peak,2047u)];
  }

